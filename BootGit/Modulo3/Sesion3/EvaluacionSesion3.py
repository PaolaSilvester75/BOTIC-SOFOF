# Importamos el módulo math para usar sus funciones matemáticas
import math

# Asignamos el valor 81 a la variable 'y'
y = 81

# Calculamos la raíz cuadrada de 'y' utilizando la función sqrt del módulo math
resultado = math.sqrt(y)

# Imprimimos en pantalla el resultado del cálculo
print("La raíz cuadrada de", y, "es:", resultado)
